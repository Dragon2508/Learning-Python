import winsound
import time
abc_morse = {       'А':'.-', 'Б':'-...','В':'.--', 'Г':'--.', 'Д':'-..','Е':'.', 'Ё':'.', 'Ж':'...-',

                    'З':'--..', 'И':'..', 'Й':'.---','К':'-.-', 'Л':'.--..', 'М':'--','Н':'-.', 'О':'---', 'П':'.--.',

                    'Р':'.-.', 'С':'...', 'Т':'-','У':'..-', 'Ф':'..-.', 'Х':'....','Ц':'-.-.', 'Ч':'---.', 'Ш':'----',

                    'Щ':'--.-', 'Ъ':'--.--', 'Ы':'-.--','Ь':'-..-', 'Э':'..-..', 'Ю':'..--','Я':'.-.-',



                    '1':'.----', '2':'..---', '3':'...--','4':'....-', '5':'.....', '6':'-....',

                    '7':'--...', '8':'---..', '9':'----.', '0':'-----',

                    
                    
                     ', ':'--..--', '.':'.-.-.-','?':'..--..', '/':'-..-.', '-':'-....-', '(':'-.--.', ')':'-.--.-'}

print("Input phrase:")
phrase = input()
phrase = phrase.upper()
morse = ""
for i in phrase:
    if i == " ":
        continue
    morse += abc_morse[i]
    morse +="   "
print(morse)
for i in morse:
    for symbol in i:
        if symbol == '.':
            winsound.Beep(1000, 100)
        elif symbol == '-':
            winsound.Beep(1000, 700)
    time.sleep(0.5)